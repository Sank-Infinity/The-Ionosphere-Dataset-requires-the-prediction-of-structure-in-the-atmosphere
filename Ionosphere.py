# Import required libraries
import numpy as np
import pandas as pd

# Importing dataset 
dataset = pd.read_csv('/content/drive/My Drive/Colab Notebooks/ionosphere.csv') # Add appropriate path

# Seperating feature of matrics and dependent variable 
X = dataset.iloc[: , :-1].values
Y = dataset.iloc[:, -1].values

# Processing output good as 1 and Bad as 0
T = []
for i in range(0,len(Y)):
  if Y[i]=='g':
    T.append(1)
  else:
    T.append(0)

# converting list format into array
T = np.asarray(T)

# Splitting dataset into training and test set
from sklearn.model_selection import train_test_split
X_train, X_test, Y_train, Y_test = train_test_split(X,T, test_size = 0.20, random_state= 0)

# Importing logistic regressor 
from sklearn.linear_model import LogisticRegression
classifier = LogisticRegression()
classifier.fit(X_train, Y_train)

# Predicting the result
Y_pred = classifier.predict(X_test)

Y_pred , Y_test

# cross validating results using confusion matrix
from sklearn.metrics import confusion_matrix
cm  = confusion_matrix(Y_test , Y_pred)

# print results 
print(cm)

# printing results in percentage 
print('{} % accuracy.'.format(((cm[0][0]+cm[1][1])/len(Y_pred))*100))

